//! Build-system helpers for translated C headers and bundled C libraries.

const std = @import("std");

pub const CoreDeps = struct {
    freetype: *std.Build.Dependency,
    harfbuzz: *std.Build.Dependency,
    freetype_config: std.Build.LazyPath,
};

pub fn resolveCoreDeps(b: *std.Build) CoreDeps {
    return .{
        .freetype = b.dependency("freetype_src", .{}),
        .harfbuzz = b.dependency("harfbuzz_src", .{}),
        .freetype_config = writeFreetypeConfig(b),
    };
}

pub fn buildFreetype(
    b: *std.Build,
    target: std.Build.ResolvedTarget,
    optimize: std.builtin.OptimizeMode,
    deps: CoreDeps,
) *std.Build.Step.Compile {
    const lib = b.addLibrary(.{
        .name = "freetype",
        .linkage = .static,
        .root_module = b.createModule(.{
            .target = target,
            .optimize = optimize,
            .link_libc = true,
        }),
    });

    addFreetypeConfig(lib.root_module, deps);
    lib.root_module.addCMacro("FT2_BUILD_LIBRARY", "1");
    lib.root_module.addCSourceFiles(.{
        .root = deps.freetype.path(""),
        .files = freetype_sources,
    });

    return lib;
}

pub fn buildHarfbuzz(
    b: *std.Build,
    target: std.Build.ResolvedTarget,
    optimize: std.builtin.OptimizeMode,
    deps: CoreDeps,
    ft_lib: *std.Build.Step.Compile,
) *std.Build.Step.Compile {
    const lib = b.addLibrary(.{
        .name = "harfbuzz",
        .linkage = .static,
        .root_module = b.createModule(.{
            .target = target,
            .optimize = optimize,
            .link_libc = true,
            .link_libcpp = true,
        }),
    });

    addFreetypeConfig(lib.root_module, deps);
    lib.root_module.addIncludePath(deps.harfbuzz.path("src"));
    lib.root_module.linkLibrary(ft_lib);
    for (harfbuzz_macros) |macro| {
        lib.root_module.addCMacro(macro.name, macro.value);
    }

    lib.root_module.addCSourceFiles(.{
        .root = deps.harfbuzz.path("src"),
        .files = &.{"harfbuzz.cc"},
        .flags = harfbuzzFlags(target.result.os.tag),
    });

    return lib;
}

pub fn translateHeavySlugC(
    b: *std.Build,
    target: std.Build.ResolvedTarget,
    optimize: std.builtin.OptimizeMode,
    deps: CoreDeps,
) *std.Build.Module {
    const translate = b.addTranslateC(.{
        .root_source_file = b.path("src/c/heavy_slug.h"),
        .target = target,
        .optimize = optimize,
    });
    translate.addIncludePath(deps.freetype_config);
    translate.addIncludePath(deps.freetype.path("include"));
    translate.addIncludePath(deps.harfbuzz.path("src"));
    translate.defineCMacro("FT_CONFIG_MODULES_H", "<heavy_slug_ftmodule.h>");
    translate.defineCMacro("FT_CONFIG_OPTIONS_H", "<heavy_slug_ftoption.h>");
    for (harfbuzz_macros) |macro| {
        translate.defineCMacro(macro.name, macro.value);
    }
    return translate.createModule();
}

const freetype_sources: []const []const u8 = &.{
    "src/base/ftbase.c",
    "src/base/ftinit.c",
    "src/base/ftsystem.c",
    "src/base/ftdebug.c",
    "src/base/ftbitmap.c",
    "src/base/ftmm.c",
    "src/truetype/truetype.c",
    "src/cff/cff.c",
    "src/cid/type1cid.c",
    "src/type1/type1.c",
    "src/type42/type42.c",
    "src/sfnt/sfnt.c",
    "src/psaux/psaux.c",
    "src/psnames/psnames.c",
};

const freetype_module_header =
    \\/* Generated by heavy-slug's Zig build.  Keep this aligned with freetype_sources. */
    \\
    \\FT_USE_MODULE( FT_Driver_ClassRec, tt_driver_class )
    \\FT_USE_MODULE( FT_Driver_ClassRec, cff_driver_class )
    \\FT_USE_MODULE( FT_Driver_ClassRec, t1cid_driver_class )
    \\FT_USE_MODULE( FT_Driver_ClassRec, t1_driver_class )
    \\FT_USE_MODULE( FT_Driver_ClassRec, t42_driver_class )
    \\FT_USE_MODULE( FT_Module_Class, sfnt_module_class )
    \\FT_USE_MODULE( FT_Module_Class, psaux_module_class )
    \\FT_USE_MODULE( FT_Module_Class, psnames_module_class )
    \\
;

const freetype_option_header =
    \\#include <freetype/config/ftoption.h>
    \\
    \\#undef FT_CONFIG_OPTION_USE_ZLIB
    \\#undef FT_CONFIG_OPTION_SYSTEM_ZLIB
    \\#undef FT_CONFIG_OPTION_USE_BZIP2
    \\#undef FT_CONFIG_OPTION_USE_PNG
    \\#undef FT_CONFIG_OPTION_USE_HARFBUZZ
    \\#undef FT_CONFIG_OPTION_USE_HARFBUZZ_DYNAMIC
    \\#undef FT_CONFIG_OPTION_USE_BROTLI
    \\#undef FT_CONFIG_OPTION_SVG
    \\
;

fn writeFreetypeConfig(b: *std.Build) std.Build.LazyPath {
    const wf = b.addWriteFiles();
    _ = wf.add("heavy_slug_ftmodule.h", freetype_module_header);
    _ = wf.add("heavy_slug_ftoption.h", freetype_option_header);
    return wf.getDirectory();
}

fn addFreetypeConfig(module: *std.Build.Module, deps: CoreDeps) void {
    module.addIncludePath(deps.freetype_config);
    module.addIncludePath(deps.freetype.path("include"));
    module.addCMacro("FT_CONFIG_MODULES_H", "<heavy_slug_ftmodule.h>");
    module.addCMacro("FT_CONFIG_OPTIONS_H", "<heavy_slug_ftoption.h>");
}

const CMacro = struct {
    name: []const u8,
    value: []const u8,
};

const harfbuzz_macros: []const CMacro = &.{
    .{ .name = "HAVE_FREETYPE", .value = "1" },
    .{ .name = "HAVE_FT_GET_VAR_BLEND_COORDINATES", .value = "1" },
    .{ .name = "HAVE_FT_SET_VAR_BLEND_COORDINATES", .value = "1" },
    .{ .name = "HAVE_FT_DONE_MM_VAR", .value = "1" },
    .{ .name = "HAVE_FT_GET_TRANSFORM", .value = "1" },
    .{ .name = "HB_DISABLE_DEPRECATED", .value = "1" },
    .{ .name = "HB_NO_BUFFER_SERIALIZE", .value = "1" },
    .{ .name = "HB_NO_BUFFER_VERIFY", .value = "1" },
    .{ .name = "HB_NO_PAINT", .value = "1" },
};

fn harfbuzzFlags(os: std.Target.Os.Tag) []const []const u8 {
    return switch (os) {
        .windows => &.{
            "-std=c++11",
            "-fno-exceptions",
            "-fno-rtti",
            "-fno-threadsafe-statics",
            "-fvisibility-inlines-hidden",
            "-Wa,-mbig-obj",
        },
        else => &.{
            "-std=c++11",
            "-fno-exceptions",
            "-fno-rtti",
            "-fno-threadsafe-statics",
            "-fvisibility-inlines-hidden",
        },
    };
}
